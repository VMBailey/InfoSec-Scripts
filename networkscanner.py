##############################################################################
# Libraries
##############################################################################
import scapy.all as scapy


##############################################################################
# Scan Function
##############################################################################
def scan(ip):
    arp_request = scapy.ARP(pdst=ip)
    print(arp_request.summary())


##############################################################################
# Main
##############################################################################
scan("192.168.1.1/24")
