##############################################################################
# Libraries
##############################################################################
import scapy.all as scapy


##############################################################################
# Sniff Function
##############################################################################
def sniff(interface):
    scapy.sniff(iface=interface, store=False, prn=process_sniffed_packet)


##############################################################################
# Packets Sniffed Function
##############################################################################
def process_sniffed_packet(packet):
    print(packet)


##############################################################################
# Main
##############################################################################
sniff("eth0")


